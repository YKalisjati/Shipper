function idbtn1() {
    var btnId = document.getElementById("btn1").value;

    
    if(btnId==1){
        document.getElementById("img1").style = "-webkit-filter: grayscale(100%);filter: grayscale(100%);";
        document.getElementById("btn1").value = "0";
    }else{
        document.getElementById("img1").style = "";
        document.getElementById("btn1").value = "1";
        
    }
}


